def _generate_erd(tables: list[dict]) -> str:
    """Generate a simple ASCII ERD."""

    lines = []

    for table in tables:
        lines.append(f"  {table['name']}")
        for col in table.get("columns", [])[:4]:  # Show first 4 columns
            pk = "PK" if col.get("primary_key") else "  "
            fk = "FK" if col.get("foreign_key") else "  "
            lines.append(f"    {pk} {fk} {col['name']} : {col['type']}")
        lines.append("")

        # Show relationships
        for rel in table.get("relationships", []):
            if rel["type"] == "one-to-many":
                lines.append(f"  1--* {rel['target']}")
            elif rel["type"] == "many-to-one":
                lines.append(f"  *--1 {table['name']}")

    return "\n".join(lines[:30])  # Limit lines


def _generate_sqlalchemy_models(tables: list[dict], database_type: str) -> str:
    """Generate SQLAlchemy model definitions."""

    code = """from sqlalchemy import Column, String, Text, Integer, Numeric, Boolean, DateTime, ForeignKey, JSON
from sqlalchemy.orm import relationship
from sqlalchemy.dialects.postgresql import UUID
from sqlalchemy.sql import func

from app.database import Base


"""

    for table in tables:
        class_name = "".join(word.capitalize() for word in table["name"].split("_"))

        code += f"class {class_name}(Base):\n"
        code += f'    __tablename__ = "{table["name"]}"\n\n'

        # Add columns
        for col in table["columns"]:
            col_def = f"    {col['name']} = Column("

            # Map types
            if col["type"].startswith("UUID"):
                col_def += "UUID(as_uuid=True), "
            elif col["type"].startswith("VARCHAR"):
                max_len = (
                    col["type"].split("(")[1].split(")")[0]
                    if "(" in col["type"]
                    else "255"
                )
                col_def += f"String({max_len}), "
            elif col["type"].startswith("TEXT"):
                col_def += "Text, "
            elif col["type"].startswith("INTEGER"):
                col_def += "Integer, "
            elif col["type"].startswith("DECIMAL"):
                col_def += "Numeric(10, 2), "
            elif col["type"].startswith("BOOLEAN"):
                col_def += "Boolean, "
            elif col["type"].startswith("TIMESTAMP"):
                col_def += "DateTime, "
            elif col["type"].startswith("JSONB"):
                col_def += "JSON, "

            # Add constraints
            if col.get("primary_key"):
                col_def += "primary_key=True, "
            if not col.get("nullable", True):
                col_def += "nullable=False, "
            if col.get("unique"):
                col_def += "unique=True, "
            if col.get("default"):
                col_def += f"default={col['default']}, "
            if col.get("foreign_key"):
                fk = col["foreign_key"]
                col_def += f"ForeignKey('{fk}'), "

            col_def = col_def.rstrip(", ") + ")\n"
            code += col_def

        # Add relationships
        for rel in table.get("relationships", []):
            if rel["type"] == "one-to-many":
                target_class = "".join(
                    word.capitalize() for word in rel["target"].split("_")
                )
                code += f"\n    {rel['target']} = relationship('{target_class}', back_populators='{table['name']}')"

        code += "\n\n"

    return code


def _generate_migration_sql(tables: list[dict], database_type: str) -> str:
    """Generate SQL migration script."""

    sql = f"-- Database Migration: {database_type}\n"
    sql += f"-- Generated: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}\n\n"

    for table in tables:
        sql += f"-- Table: {table['name']}\n"
        sql += f"CREATE TABLE {table['name']} (\n"

        col_defs = []
        for col in table["columns"]:
            col_def = f"    {col['name']} {col['type']}"

            if col.get("primary_key"):
                col_def += " PRIMARY KEY"
            if not col.get("nullable", True):
                col_def += " NOT NULL"
            if col.get("unique"):
                col_def += " UNIQUE"
            if col.get("default"):
                col_def += f" DEFAULT {col['default']}"
            if col.get("foreign_key"):
                col_def += f" REFERENCES {col['foreign_key']}"

            col_defs.append(col_def)

        sql += ",\n".join(col_defs)
        sql += "\n);\n"

        # Add indexes
        for idx in table.get("indexes", []):
            sql += (
                f"CREATE INDEX {idx['name']} ON {table['name']} ({idx['columns']});\n"
            )

        sql += "\n"

    return sql
