"""
Database Schema Generator - Generates Database_schema.md for Phase 1

This module creates a comprehensive database schema document based on the
plan.md and tasks.md files created during Phase 1 planning.

The generated Database_schema.md includes:
- Complete database schema with all tables
- Column definitions with types and constraints
- Relationships between tables
- Indexes and constraints
- SQLAlchemy models (optional)
- Migration scripts (optional)

Used by Phase 3 subagent-2 for backend development.
"""

from typing import Any
from datetime import datetime


def generate_database_schema(
    plan_content: str,
    tasks_content: str,
    project_name: str = "Project",
    database_type: str = "PostgreSQL",
) -> str:
    """
    Generate a comprehensive database schema document.

    Args:
        plan_content: Content of plan.md
        tasks_content: Content of tasks.md
        project_name: Name of the project
        database_type: Database type (PostgreSQL, MySQL, MongoDB, etc.)

    Returns:
        Markdown-formatted database schema document
    """

    # Extract tables from plan and tasks
    tables = _extract_tables(plan_content, tasks_content)

    # Build the document
    doc = f"""# Database Schema - {project_name}

> Generated by Pakalon Phase 1
> Database: {database_type}
> Generated: {datetime.now().strftime("%Y-%m-%d %H:%M:%S")}

## Table of Contents

1. [Overview](#overview)
2. [Entity Relationship Diagram](#entity-relationship-diagram)
3. [Tables](#tables)
4. [Indexes](#indexes)
5. [Relationships](#relationships)
6. [SQLAlchemy Models](#sqlalchemy-models)
7. [Migration Scripts](#migration-scripts)

---

## Overview

This document provides the complete database schema for **{project_name}**.

### Database Information

| Property | Value |
|----------|-------|
| Database Type | {database_type} |
| Encoding | UTF-8 |
| Timezone | UTC |

---

## Entity Relationship Diagram

```
{_generate_erd(tables)}
```

---

## Tables

"""

    # Add each table
    for table in tables:
        doc += f"""
### {table["name"]}

{table.get("description", f"{table['name']} table")}

| Column | Type | Constraints | Description |
|--------|------|-------------|-------------|
"""

        for col in table.get("columns", []):
            constraints = []
            if col.get("primary_key"):
                constraints.append("PRIMARY KEY")
            if col.get("unique"):
                constraints.append("UNIQUE")
            if not col.get("nullable", True):
                constraints.append("NOT NULL")
            if col.get("default"):
                constraints.append(f"DEFAULT {col['default']}")
            if col.get("foreign_key"):
                constraints.append(f"REFERENCES {col['foreign_key']}")

            constraints_str = ", ".join(constraints) if constraints else "-"

            doc += f"| {col['name']} | {col['type']} | {constraints_str} | {col.get('description', '')} |\n"

        # Add sample data section
        if table.get("sample_data"):
            doc += f"""
#### Sample Data

```
{table["sample_data"]}
```
"""

    # Add indexes section
    doc += "\n## Indexes\n\n"
    doc += "| Table | Index Name | Columns | Type |\n"
    doc += "|-------|------------|---------|------|\n"

    for table in tables:
        for idx in table.get("indexes", []):
            doc += f"| {table['name']} | {idx['name']} | {idx['columns']} | {idx.get('type', 'BTREE')} |\n"

    # Add relationships section
    doc += "\n## Relationships\n\n"
    doc += "| From | To | Type | Description |\n"
    doc += "|------|-----|------|-------------|\n"

    for table in tables:
        for rel in table.get("relationships", []):
            doc += f"| {table['name']} | {rel['target']} | {rel['type']} | {rel.get('description', '')} |\n"

    # Add SQLAlchemy models
    doc += "\n## SQLAlchemy Models\n\n"
    doc += "```python\n"
    doc += _generate_sqlalchemy_models(tables, database_type)
    doc += "\n```\n"

    # Add migration script
    doc += "\n## Migration Scripts\n\n"
    doc += f"### Create All Tables ({database_type})\n\n"
    doc += "```sql\n"
    doc += _generate_migration_sql(tables, database_type)
    doc += "\n```\n"

    doc += f"""

---

Generated by Pakalon AI Agent - Phase 1
"""

    return doc


